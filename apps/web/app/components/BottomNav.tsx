const HIDDEN_ON = ["/login", "/register"];

export function BottomNav() {
  const pathname = usePathname();
  if (HIDDEN_ON.some((p) => pathname.startsWith(p))) return null;

  return (
    <nav className="gym-bottom-nav fixed bottom-6 left-1/2 z-40 -translate-x-1/2">
      <div className="flex items-center gap-0.5 rounded-[28px] border border-white/[0.07] bg-[#111111]/95 px-2 py-2 shadow-2xl shadow-black/80 backdrop-blur-xl">
        {tabs.map((tab) => {
          const active =
            tab.href === "/gym"
              ? pathname === "/gym" || pathname === "/"
              : pathname.startsWith(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              aria-label={tab.label}
              className={`relative flex h-12 w-12 items-center justify-center rounded-[18px] transition-all ${
                active
                  ? "text-accent bg-accent/[0.08]"
                  : "text-muted/50 hover:text-muted"
              }`}
            >
              {tab.icon}
              {active && (
                <span className="absolute bottom-1.5 left-1/2 h-0.5 w-3 -translate-x-1/2 rounded-full bg-accent" />
              )}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
